public class MathClac {
    public static void main(String[] args){
        System.out.println(5+6); //加
        System.out.println(5-6); //减
        System.out.println(5*6); //乘
        System.out.println(5/6.0); //除

        // 自加
        int n = 12;
        n++;
        System.out.println(n);
    }
}
